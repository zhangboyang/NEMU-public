// functions that convert numbers to ascii md5 string

void md5_write_hexnumber(char *outstr, unsigned x)
{
    const char *dict = "0123456789abcdef";
    outstr[0] = dict[(x / 16) % 16];
    outstr[1] = dict[x % 16];
}

void md5_write_string(char *outstr, unsigned x)
{
    unsigned a, b, c, d;
    a = x & 0xff;
    b = (x >> 8) & 0xff;
    c = (x >> 16) & 0xff;
    d = x >> 24;
    md5_write_hexnumber(outstr, a);
    md5_write_hexnumber(outstr + 2, b);
    md5_write_hexnumber(outstr + 4, c);
    md5_write_hexnumber(outstr + 6, d);
}


