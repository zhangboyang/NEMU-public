// md5 algorithm

unsigned a0, b0, c0, d0;

unsigned md5_rotate(unsigned x, unsigned y)
{
// NEMU hack: to avoid the ROL instruction
    volatile unsigned high, low;
    high = x << y;
    low = x >> (32 - y);
    return high | low;

//    return (x << y) | (x >> (32 - y));
}

void md5_init()
{
    a0 = 0x67452301;
    b0 = 0xefcdab89;
    c0 = 0x98badcfe;
    d0 = 0x10325476;
}

void md5_process_trunk(unsigned *M)
{
    unsigned i;
    unsigned A, B, C, D, F, G, D2;
    
    A = a0;
    B = b0;
    C = c0;
    D = d0;

    for (i = 0; i < 64; i++) {
        if (i < 16) {
            F = (B & C) | (~B & D);
            G = i;
        } else if (i < 32) {
            F = (D & B) | (~D & C);
            G = (5 * i + 1) % 16;
        } else if (i < 48) {
            F = B ^ C ^ D;
            G = (3 * i + 5) % 16;
        } else {
            F = C ^ (B | ~D);
            G = (7 * i) % 16;
        }
        D2 = D;
        D = C;
        C = B;
        B = B + md5_rotate(A + F + K[i] + M[G], S[i]);
        A = D2;
    }

    a0 += A;
    b0 += B;
    c0 += C;
    d0 += D;
}

void md5_run(char *outstr, const void *data, int datalen)
{
    const char *ptr = data;
    int len = datalen;

    unsigned buf[16];

    md5_init();

    while (len >= 64) {
        memcpy(buf, ptr, 64);
        md5_process_trunk(buf);
        ptr += 64;
        len -= 64;
    }

    memset(buf, 0, sizeof(buf));
    memcpy(buf, ptr, len);
    ((char *) buf)[len++] = 0x80;
    
    if (len <= 56) {
        buf[14] = datalen * 8; // FIXME
        md5_process_trunk(buf);
    } else {
        md5_process_trunk(buf);
        memset(buf, 0, sizeof(buf));
        buf[14] = datalen * 8; // FIXME
        md5_process_trunk(buf);
    }
    
    md5_write_string(outstr, a0);
    md5_write_string(outstr + 8, b0);
    md5_write_string(outstr + 16, c0);
    md5_write_string(outstr + 24, d0);
    outstr[32] = 0;
}



